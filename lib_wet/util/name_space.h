const int NameSpaceChunkDataSize=4096;




/*+-----------------------------------------------------------------------------------------------------------------+*/
//
// a memory chunk of names
//
/*+-----------------------------------------------------------------------------------------------------------------+*/
struct name_space_chunk
{

/*+-----------------------------------------------------------------------------------------------------------------+*/

	struct name_space_chunk *next; //next chained structure for when we run out of space in this one

	char data[NameSpaceChunkDataSize]; //buncho strings

};
/*+-----------------------------------------------------------------------------------------------------------------+*/





/*+-----------------------------------------------------------------------------------------------------------------+*/
//
// isaspace for keeping names in
//
/*+-----------------------------------------------------------------------------------------------------------------+*/
struct name_space
{

bool setup(void);
bool reset(void);
void clean(void);

/*+-----------------------------------------------------------------------------------------------------------------+*/

	struct name_space_chunk *chunk; //pointer to linked chunks of memory to store names in

	bool ignore_case;

/*+-----------------------------------------------------------------------------------------------------------------+*/

const char * Make(const char *name);
const char * Find(const char *name);

/*+-----------------------------------------------------------------------------------------------------------------+*/

name_space(void)
{
	setup();
}

~name_space(void)
{
	clean();
}

};
