#include "all.h"







/*------------------------------------------------------------------------------------------------------------------------------*/
//
// copy some memory
//
/*------------------------------------------------------------------------------------------------------------------------------*/
void dmem_copy( cu8* from , u8* dest , int size)
{
	memcpy((void*)dest,(const void*)from,size);
}


/*------------------------------------------------------------------------------------------------------------------------------*/
//
// set some memory
//
/*------------------------------------------------------------------------------------------------------------------------------*/
void dmem_set( u8 dat , u8* dest , int size)
{
	memset((void*)dest,dat,size);
}

