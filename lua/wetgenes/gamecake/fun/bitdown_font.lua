-- build a bitmap which is a 128x1 array of characters using above fontdata
M.build_grd=function(hx,hy)

	local name=hx.."x"..hy
	
	if not M.grds[name] then -- render
	
		local data=assert(M["fontdata"..hx.."x"..hy]) -- get data and check size is valid

		local g=wgrd.create("U8_RGBA_PREMULT",128*hx,1*hy,1)
		
		for i=0,127 do
			local s=data[i]
			if s then
				bitdown.pix_grd(s,bitdown.cmap_swanky32,g,i*hx,0,hx,hy)
			end
		end
		
		M.grds[name]=g

	end

	return M.grds[name]
end

